export function searchFAQs(query: string): { item: FAQItem; categoryLabel: string; categoryIcon: string }[] {
  if (!query.trim()) return [];
  const q = query.toLowerCase();
  const results: { item: FAQItem; categoryLabel: string; categoryIcon: string }[] = [];
  for (const cat of FAQ_CATEGORIES) {
    for (const item of cat.items) {
      if (item.q.toLowerCase().includes(q) || item.a.toLowerCase().includes(q)) {
        results.push({ item, categoryLabel: cat.label, categoryIcon: cat.icon });
      }
    }
  }
  return results;
}
